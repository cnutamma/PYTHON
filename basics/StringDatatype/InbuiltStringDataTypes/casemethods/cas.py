'''
there are 5 case methods which are in built methods in strings
1)upper()-converts each letter into uppercase
2)lower()-converts each letter into smallercase
3)capitalize()-first letter of the string is capital
4)title()-Every word first letter is capital
5)swapcase()-opposite to the given string if it is lower-upper&upper-lower
'''

ename="hello srinivas"

print(ename.upper())
print(ename.lower())
print(ename.capitalize())
print(ename.title())
print(ename.swapcase())